double detM3x3(double (&oM)[3][3]) {
   double res=oM[0][0]*(oM[1][1]*oM[2][2]-oM[2][1]*oM[1][2]);
   res+=(oM[0][1]*(oM[2][0]*oM[1][2]-oM[1][0]*oM[2][2]));
   res+=(oM[0][2]*(oM[1][0]*oM[2][1]-oM[2][0]*oM[1][1]));
   return res;
}
